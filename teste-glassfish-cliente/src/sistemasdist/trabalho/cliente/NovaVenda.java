    public void refreshProduto(){
        Long idProd = Long.parseLong(this.jTextField3.getText());
        
        InitialContext ctx;
        try {
            ctx = new InitialContext();
            ProdutoRemote ejbProduto = (ProdutoRemote) ctx.lookup("sistemasdist.trabalho.ejb.ProdutoRemote");
            
            this.produtoMain = ejbProduto.consultarPorId(idProd);
            if(this.produtoMain != null){
                this.jTextField6.setText(this.produtoMain.getDescricao());
            }else{
                this.jTextField6.setText(null);
            }
            
        } catch (NamingException ex) {
            Logger.getLogger(NovaVenda.class.getName()).log(Level.SEVERE, null, ex);
        }
           
    }   
    
    public void refreshPessoa(){
        Long idPessoa = Long.parseLong(this.jTextField2.getText());
        
        InitialContext ctx;
        try {
            ctx = new InitialContext();
            PessoaRemote ejbPessoa = (PessoaRemote) ctx.lookup("sistemasdist.trabalho.ejb.PessoaRemote");
            
            this.pessoaMain = ejbPessoa.consultarPorId(idPessoa);
            if(this.pessoaMain != null){
                this.jTextField5.setText(this.pessoaMain.getNome());
            }else{
                this.jTextField5.setText(null);
            }
            
        } catch (NamingException ex) {
            Logger.getLogger(NovaVenda.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

