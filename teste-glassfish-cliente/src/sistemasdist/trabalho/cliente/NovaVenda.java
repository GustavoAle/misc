    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat data = new SimpleDateFormat("dd/MM/yyyy");

        try {
            this.compraMain.setDataVenda(data.parse(this.jTextField4.getText()));
        } catch (ParseException ex) {
            Logger.getLogger(NovaVenda.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        this.compraMain.setPessoa(this.pessoaMain);
        this.compraMain.setProduto(this.produtoMain);
        
        System.out.println("Salvando: "+this.compraMain.getId());
        
        InitialContext ctx;
        try {
            ctx = new InitialContext();
            CompraRemote ejbCompra = (CompraRemote) ctx.lookup("sistemasdist.trabalho.ejb.CompraRemote");

            try {
                ejbCompra.salvar(this.compraMain);
            } catch (Exception ex) {
                Logger.getLogger(CadastroProduto.class.getName()).log(Level.SEVERE, null, ex);
            }

        } catch (NamingException ex) {
            Logger.getLogger(CadastroProduto.class.getName()).log(Level.SEVERE, null, ex);
        }

        this.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

