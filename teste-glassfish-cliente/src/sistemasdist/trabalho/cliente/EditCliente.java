    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat data = new SimpleDateFormat("dd/MM/yyyy");

        try {
            this.pessoaMain.setDataNascimento(data.parse(this.jTextField3.getText()));
        } catch (ParseException ex) {
            Logger.getLogger(EditCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        this.pessoaMain.setNome(this.jTextField2.getText());
        this.pessoaMain.setEndereco(this.jTextField4.getText());

        
        System.out.println("Salvando: "+this.pessoaMain.getId());
        
        InitialContext ctx;
        try {
            ctx = new InitialContext();
            PessoaRemote ejbPessoa = (PessoaRemote) ctx.lookup("sistemasdist.trabalho.ejb.PessoaRemote");

            try {
                ejbPessoa.salvar(this.pessoaMain);
            } catch (Exception ex) {
                Logger.getLogger(CadastroProduto.class.getName()).log(Level.SEVERE, null, ex);
            }

        } catch (NamingException ex) {
            Logger.getLogger(CadastroProduto.class.getName()).log(Level.SEVERE, null, ex);
        }

        this.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

