    public void clearFields(){
        this.jTextField1.setText(null);
        this.jTextField2.setText(null);
        this.jTextField3.setText(null);
        this.jTextField4.setText(null);
        
        this.pessoaMain.setId(null);
    }
    
    public void editPessoa(Pessoa p){
        SimpleDateFormat date = new SimpleDateFormat("dd/MM/yyyy");
        
        this.pessoaMain.setId(p.getId());
        this.pessoaMain.setNome(p.getNome());
        this.pessoaMain.setDataNascimento(p.getDataNascimento());
        this.pessoaMain.setEndereco(p.getEndereco());
        
        this.jTextField1.setText(p.getId().toString());
        this.jTextField2.setText(p.getNome());
        this.jTextField4.setText(p.getEndereco());
        
        if(p.getDataNascimento() != null){
            
            this.jTextField3.setText(date.format(p.getDataNascimento()));
        }else{
            this.jTextField3.setText(null);
        }
    }
    
